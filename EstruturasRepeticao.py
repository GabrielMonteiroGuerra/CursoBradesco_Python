#LAÇO DE REPETIÇÃO FOR

for n in range(10):
    print(n)

print('=======================================')  #Começa sempre do zero

for a in range(2,10): #Vai começar a partir do 2
    print(a)

print('==============================================================')

for b in range(10,0,-2): #Vai começar do 10, diminuir até o > 0 e decrementar sempre -2
    print(b)

print('=================================================')

